// creation array playlist with 3 songs
const playlist = ["I Will Survive", "I Gotta Feeling", "Les Sardines"]

console.log(playlist)
console.log(playlist.length)

// we add 2 songs with push()
playlist.push("Free From Desire", "Fever")
console.log(playlist)
console.log(playlist.length)

// we remove last song with pop()
playlist.pop()
console.log(playlist)
console.log(playlist.length)